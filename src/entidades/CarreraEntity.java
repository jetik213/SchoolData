package entidades;

public class CarreraEntity {
	
	private int carrId;
	private String carrNombre;
	private int facuId;
	private int carrEstado;
	
	public int getCarrId() {
		return carrId;
	}
	public void setCarrId(int carrId) {
		this.carrId = carrId;
	}
	public String getCarrNombre() {
		return carrNombre;
	}
	public void setCarrNombre(String carrNombre) {
		this.carrNombre = carrNombre;
	}
	public int getFacuId() {
		return facuId;
	}
	public void setFacuId(int facuId) {
		this.facuId = facuId;
	}
	public int getCarrEstado() {
		return carrEstado;
	}
	public void setCarrEstado(int carrEstado) {
		this.carrEstado = carrEstado;
	}	

}
