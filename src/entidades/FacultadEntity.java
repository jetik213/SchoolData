package entidades;

public class FacultadEntity {

	private int facuID;
	private String facuNombre;
	private int facuEstado;
	
	public int getFacuID() {
		return facuID;
	}
	public void setFacuID(int facuID) {
		this.facuID = facuID;
	}
	public String getFacuNombre() {
		return facuNombre;
	}
	public void setFacuNombre(String facuNombre) {
		this.facuNombre = facuNombre;
	}
	public int getFacuEstado() {
		return facuEstado;
	}
	public void setFacuEstado(int facuEstado) {
		this.facuEstado = facuEstado;
	}
	
	
}
